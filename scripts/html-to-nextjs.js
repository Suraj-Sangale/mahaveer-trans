#!/usr/bin/env node
/**
 * ╔══════════════════════════════════════════════════════════════════╗
 * ║       HTML → Next.js Page Converter                             ║
 * ║  Usage: node scripts/html-to-nextjs.js <path-to-html> [route]  ║
 * ║                                                                  ║
 * ║  Examples:                                                       ║
 * ║    node scripts/html-to-nextjs.js components/about.html         ║
 * ║    node scripts/html-to-nextjs.js components/contact.html       ║
 * ║    node scripts/html-to-nextjs.js components/service.html       ║
 * ║                                                                  ║
 * ║  What it does:                                                   ║
 * ║  1. Extracts <style> → app/<name>.css                           ║
 * ║  2. Extracts SITE_DATA from <script> → app/<route>/data.ts      ║
 * ║  3. Converts <body> HTML → JSX (class→className, etc.)          ║
 * ║  4. Generates app/<route>/page.tsx with React hooks              ║
 * ║  5. Updates app/layout.tsx to import the new CSS                ║
 * ╚══════════════════════════════════════════════════════════════════╝
 */

const fs = require("fs");
const path = require("path");

// ─── CLI args ──────────────────────────────────────────────────────────────────
const [, , inputFile, routeArg] = process.argv;

if (!inputFile) {
  console.error("❌  Usage: node scripts/html-to-nextjs.js <path-to-html> [route-name]");
  console.error("   Example: node scripts/html-to-nextjs.js components/about.html about");
  process.exit(1);
}

const htmlPath = path.resolve(inputFile);
if (!fs.existsSync(htmlPath)) {
  console.error(`❌  File not found: ${htmlPath}`);
  process.exit(1);
}

// Derive route name from filename if not provided (e.g. "about.html" → "about")
const baseName = path.basename(htmlPath, ".html").replace(/[^a-z0-9-_]/gi, "-").toLowerCase();
const routeName = routeArg || baseName;
const isPrimary = routeName === "index" || routeName === "home" || routeName === "landingpage";

// Project root = parent of /scripts
const projectRoot = path.join(__dirname, "..");
const appDir = path.join(projectRoot, "app");
const outDir = isPrimary ? appDir : path.join(appDir, routeName);

console.log(`\n🚀 HTML → Next.js Converter`);
console.log(`   Input : ${htmlPath}`);
console.log(`   Route : ${isPrimary ? "/" : "/" + routeName}`);
console.log(`   Output: ${outDir}\n`);

// ─── Read file ─────────────────────────────────────────────────────────────────
const raw = fs.readFileSync(htmlPath, "utf-8");

// ─── STEP 1: Extract <style> block ─────────────────────────────────────────────
console.log("📦 Step 1: Extracting CSS...");
const styleMatch = raw.match(/<style[^>]*>([\s\S]*?)<\/style>/i);
const css = styleMatch ? styleMatch[1] : "";
const cssFileName = `${routeName}.css`;
const cssOutPath = path.join(appDir, cssFileName);

if (css) {
  fs.writeFileSync(cssOutPath, css, "utf-8");
  console.log(`   ✅ CSS  → app/${cssFileName} (${(css.length / 1024).toFixed(1)} KB)`);
} else {
  console.log("   ⚠️  No <style> block found — skipping CSS file.");
}

// ─── STEP 2: Extract SITE_DATA from <script> block ────────────────────────────
console.log("📦 Step 2: Extracting data...");
const scriptMatch = raw.match(/<script[^>]*>([\s\S]*?)<\/script>/i);
const script = scriptMatch ? scriptMatch[1] : "";

// Match: const SITE_DATA = { ... }; (handles nested braces)
let siteData = "";
const dataStart = script.indexOf("const SITE_DATA");
if (dataStart !== -1) {
  let depth = 0;
  let inStr = false, strChar = "";
  let i = script.indexOf("{", dataStart);
  const start = i;
  for (; i < script.length; i++) {
    const ch = script[i];
    if (inStr) {
      if (ch === strChar && script[i - 1] !== "\\") inStr = false;
    } else if (ch === '"' || ch === "'" || ch === "`") {
      inStr = true; strChar = ch;
    } else if (ch === "{") depth++;
    else if (ch === "}") { depth--; if (depth === 0) { siteData = script.slice(start, i + 1); break; } }
  }
}

if (!fs.existsSync(outDir)) fs.mkdirSync(outDir, { recursive: true });

if (siteData) {
  // Format cleanly and produce a typed export
  const dataTs = `// Auto-generated by scripts/html-to-nextjs.js
// Edit this file to update page content without touching JSX.

export const SITE_DATA = ${siteData} as const;

export type SiteData = typeof SITE_DATA;
`;
  fs.writeFileSync(path.join(outDir, "data.ts"), dataTs, "utf-8");
  console.log(`   ✅ Data → app/${routeName}/data.ts`);
} else {
  // No SITE_DATA — create a placeholder
  const placeholder = `// Auto-generated by scripts/html-to-nextjs.js
// No SITE_DATA found in the source HTML.
// Add your page data here.

export const SITE_DATA = {
  meta: { title: "${routeName.charAt(0).toUpperCase() + routeName.slice(1)}" },
} as const;
`;
  fs.writeFileSync(path.join(outDir, "data.ts"), placeholder, "utf-8");
  console.log("   ⚠️  No SITE_DATA found — created placeholder data.ts");
}

// ─── STEP 3: Extract JS interactive logic ─────────────────────────────────────
console.log("📦 Step 3: Analysing JS logic...");

const hasThemeToggle = script.includes("toggleTheme") || script.includes("data-theme");
const hasHamburger = script.includes("hamburger") || script.includes("mobileDrawer");
const hasFontPanel = script.includes("fontPanel") || script.includes("fp-font-opt");
const hasCountUp = script.includes("setInterval") && script.includes("data-target");
const hasReveal = script.includes("IntersectionObserver") && script.includes("vis");
const hasTrackBtn = script.includes("trackBtn") || script.includes("t-input");
const hasScrollNav = script.includes("scrolled");
const hasAccentPicker = script.includes("fp-color");

console.log(`   Theme toggle  : ${hasThemeToggle ? "✅" : "—"}`);
console.log(`   Mobile drawer : ${hasHamburger ? "✅" : "—"}`);
console.log(`   Font panel    : ${hasFontPanel ? "✅" : "—"}`);
console.log(`   Counter anim  : ${hasCountUp ? "✅" : "—"}`);
console.log(`   Scroll reveal : ${hasReveal ? "✅" : "—"}`);
console.log(`   Track form    : ${hasTrackBtn ? "✅" : "—"}`);
console.log(`   Scroll navbar : ${hasScrollNav ? "✅" : "—"}`);
console.log(`   Accent picker : ${hasAccentPicker ? "✅" : "—"}`);

// ─── STEP 4: Extract <body> and convert to JSX ────────────────────────────────
console.log("📦 Step 4: Converting HTML body to JSX...");

// Get body content (between <body> and <script>)
const bodyMatch = raw.match(/<body[^>]*>([\s\S]*?)(?:<script|<\/body>)/i);
const bodyHtml = bodyMatch ? bodyMatch[1].trim() : "";

// ── HTML → JSX attribute mapping ──────────────────────────────────────────────
function htmlToJsx(html) {
  return html
    // Self-closing void elements
    .replace(/<(area|base|br|col|embed|hr|img|input|link|meta|param|source|track|wbr)([^>]*?)(?:\/)?>(\s*)/gi,
      (_, tag, attrs, space) => `<${tag}${convertAttrs(attrs)} />${space}`)
    // Convert attributes on all other tags
    .replace(/<([a-z][a-z0-9-]*)([^>]*)>/gi,
      (_, tag, attrs) => `<${tag}${convertAttrs(attrs)}>`)
    // HTML entities in JSX strings
    .replace(/&amp;/g, "&")
    .replace(/&nbsp;/g, " ")
    .replace(/<!-- (.*?) -->/g, "{/* $1 */}")
    // Empty innerHTML targets — replace with {/* DYNAMIC */} comment hint
    .replace(/ id="([^"]+)"(?=[^>]*><\/)/g, ' id="$1" {/* TODO: dynamic */}');
}

function convertAttrs(attrStr) {
  return attrStr
    // class → className
    .replace(/\bclass=/g, "className=")
    // for → htmlFor
    .replace(/\bfor=/g, "htmlFor=")
    // tabindex → tabIndex
    .replace(/\btabindex=/g, "tabIndex=")
    // Inline style strings → JSX style objects (basic heuristic)
    .replace(/style="([^"]*)"/g, (_, styleStr) => {
      const obj = styleStr
        .split(";")
        .filter(Boolean)
        .map((decl) => {
          const [prop, ...rest] = decl.split(":");
          const value = rest.join(":").trim();
          const jsProp = prop.trim().replace(/-([a-z])/g, (_, c) => c.toUpperCase());
          return `${jsProp}: "${value}"`;
        })
        .join(", ");
      return `style={{ ${obj} }}`;
    })
    // onclick → onClick
    .replace(/\bon([a-z]+)=/gi, (_, ev) => `on${ev.charAt(0).toUpperCase() + ev.slice(1)}=`)
    // Remove inline event handlers pointing to JS functions (flag for manual fix)
    .replace(/on[A-Z][a-z]+="[^"]*\(\)"/g, (match) => `{/* TODO: ${match} */}`)
    // Boolean attributes
    .replace(/\b(disabled|checked|selected|readonly|required|multiple|autofocus|autoplay|controls|loop|muted)\b(?!=)/gi,
      '$1={true}')
    // href with .html → Next.js friendly href
    .replace(/href="([^"#][^"]*?)\.html"/g, 'href="/$1"');
}

// ─── STEP 4: Convert HTML body to JSX
const jsx = htmlToJsx(bodyHtml);

// Identify dynamic element IDs from the original HTML
const dynamicIds = [...bodyHtml.matchAll(/id="([^"]+)"\>\<\/[a-z]/gi)].map((m) => m[1]);

// Build a map of dynamic ID → SITE_DATA.meta (if it exists)
const dynamicMap = {};
dynamicIds.forEach(id => {
  if (SITE_DATA.meta && id in SITE_DATA.meta) {
    dynamicMap[id] = SITE_DATA.meta[id];
  } else {
    dynamicMap[id] = ""; // fallback empty string
  }
});

// Inject dynamic data into the generated JSX
let finalJsx = jsx.replace(/id="([^\"]+)"(?=[^>]*>\s*<\/[^>]+>)/g, (_, id) => {
  const comment = dynamicMap[id] ? dynamicMap[id] : "TODO: fill data";
  return `id="${id}" {/* ${comment} */}`;
});

// ─── STEP 6: Build imports block ──────────────────────────────────────────────
const hooksNeeded = ["useEffect"];
if (hasThemeToggle || hasFontPanel || hasHamburger || hasTrackBtn || hasAccentPicker) hooksNeeded.push("useState");
if (hasCountUp) hooksNeeded.push("useRef");
if (hasThemeToggle || hasFontPanel) hooksNeeded.push("useCallback");

const imports = `"use client";

import { ${[...new Set(hooksNeeded)].join(", ")} } from "react";
import { SITE_DATA } from "./data";
`;

// ─── STEP 7: Build hooks section ──────────────────────────────────────────────
function buildHooks() {
  const parts = [];

  if (hasThemeToggle) {
    parts.push(`
  // ── Theme (dark / light) ──────────────────────────────────────────
  const [theme, setTheme] = useState<"light" | "dark">("light");
  useEffect(() => {
    const saved = localStorage.getItem("vf-theme") as "light" | "dark" | null;
    if (saved) setTheme(saved);
  }, []);
  useEffect(() => {
    document.documentElement.dataset.theme = theme;
    localStorage.setItem("vf-theme", theme);
  }, [theme]);
  const toggleTheme = useCallback(() => setTheme((t) => (t === "dark" ? "light" : "dark")), []);`);
  }

  if (hasHamburger) {
    parts.push(`
  // ── Mobile drawer ─────────────────────────────────────────────────
  const [drawerOpen, setDrawerOpen] = useState(false);
  const openDrawer = () => { setDrawerOpen(true); document.body.style.overflow = "hidden"; };
  const closeDrawer = () => { setDrawerOpen(false); document.body.style.overflow = ""; };`);
  }

  if (hasFontPanel) {
    parts.push(`
  // ── Font panel ────────────────────────────────────────────────────
  const [fontPanelOpen, setFontPanelOpen] = useState(false);
  const [displayFont, setDisplayFont] = useState("Cormorant Garamond");
  const [bodyFont, setBodyFont] = useState("Plus Jakarta Sans");
  useEffect(() => {
    document.documentElement.style.setProperty("--font-display", \`'\${displayFont}', serif\`);
  }, [displayFont]);
  useEffect(() => {
    document.documentElement.style.setProperty("--font-body", \`'\${bodyFont}', sans-serif\`);
  }, [bodyFont]);`);
  }

  if (hasAccentPicker) {
    parts.push(`
  // ── Accent color picker ───────────────────────────────────────────
  const [accentColor, setAccentColor] = useState("#0ea5e9");
  useEffect(() => {
    const shade = (hex: string, pct: number) => {
      const n = parseInt(hex.slice(1), 16);
      const clamp = (v: number) => Math.min(255, Math.max(0, v));
      const r = clamp(((n >> 16) & 0xff) + pct);
      const g = clamp(((n >> 8) & 0xff) + pct);
      const b = clamp((n & 0xff) + pct);
      return "#" + (r << 16 | g << 8 | b).toString(16).padStart(6, "0");
    };
    const r = document.documentElement;
    r.style.setProperty("--accent", accentColor);
    r.style.setProperty("--accent-dk", shade(accentColor, -15));
  }, [accentColor]);`);
  }

  if (hasScrollNav) {
    parts.push(`
  // ── Navbar scroll shadow ──────────────────────────────────────────
  const [scrolled, setScrolled] = useState(false);
  useEffect(() => {
    const handler = () => setScrolled(window.scrollY > 40);
    window.addEventListener("scroll", handler);
    return () => window.removeEventListener("scroll", handler);
  }, []);`);
  }

  if (hasReveal) {
    parts.push(`
  // ── Scroll-reveal animations ──────────────────────────────────────
  useEffect(() => {
    const io = new IntersectionObserver(
      (entries) => entries.forEach((e) => { if (e.isIntersecting) e.target.classList.add("vis"); }),
      { threshold: 0.1 }
    );
    document.querySelectorAll(".reveal,.svc-card,.pstep,.tc").forEach((el) => io.observe(el));
    return () => io.disconnect();
  }, []);`);
  }

  if (hasTrackBtn) {
    parts.push(`
  // ── Tracking / search form ────────────────────────────────────────
  const [trackValue, setTrackValue] = useState("");
  const [trackState, setTrackState] = useState<"idle" | "found" | "error">("idle");
  const handleTrack = () => {
    if (trackValue.trim()) {
      setTrackState("found");
      setTimeout(() => setTrackState("idle"), 2500);
    } else {
      setTrackState("error");
      setTimeout(() => setTrackState("idle"), 2000);
    }
  };`);
  }

  return parts.join("\n");
}

// ─── STEP 8: Assemble page.tsx ────────────────────────────────────────────────
console.log("📦 Step 5: Assembling page.tsx...");

// Find page title
const titleMatch = raw.match(/<title[^>]*>(.*?)<\/title>/i);
const pageTitle = titleMatch ? titleMatch[1].trim() : routeName;

// Build dynamic IDs notice
const dynamicNote = dynamicIds.length > 0
  ? `\n  /*
   * TODO: The following element IDs had empty inner content in the HTML.
   * They were originally filled via JS DOM manipulation.
   * Replace the content with data from SITE_DATA:
   *   ${dynamicIds.join("\n   *   ")}
   */`
  : "";

const pageTsx = `${imports}
// ─── ${pageTitle} ─────────────────────────────────────────────────────────────
// Auto-converted from: ${path.basename(htmlPath)}
// by scripts/html-to-nextjs.js
//
// HOW TO COMPLETE THIS FILE:
// 1. Check the TODO comments — they mark elements that need dynamic data from SITE_DATA.
// 2. Replace empty JSX elements with data props (e.g., {d.hero.title}).
// 3. Remove or replace any {/* TODO: onclick */} with proper React event handlers.
// 4. Review the data.ts file and update SITE_DATA with your real content.

  return (
    <>${finalJsx.split("\n").map((l) => "      " + l).join("\n")}</>);

`;

const pagePath = path.join(outDir, "page.tsx");
fs.writeFileSync(pagePath, pageTsx, "utf-8");
console.log(`   ✅ Page → app/${routeName}/page.tsx`);

// ─── STEP 9: Update layout.tsx to import new CSS ──────────────────────────────
if (css) {
  console.log("📦 Step 6: Updating layout.tsx CSS imports...");
  const layoutPath = path.join(appDir, "layout.tsx");
  if (fs.existsSync(layoutPath)) {
    let layout = fs.readFileSync(layoutPath, "utf-8");
    const importLine = `import "./${cssFileName}";`;
    if (!layout.includes(importLine)) {
      // Insert after last import statement
      const lastImport = layout.lastIndexOf("import ");
      const insertAt = layout.indexOf("\n", lastImport) + 1;
      layout = layout.slice(0, insertAt) + importLine + "\n" + layout.slice(insertAt);
      fs.writeFileSync(layoutPath, layout, "utf-8");
      console.log(`   ✅ Added: import "./${cssFileName}" to layout.tsx`);
    } else {
      console.log(`   ℹ️  layout.tsx already imports ${cssFileName}`);
    }
  }
}

// ─── DONE ─────────────────────────────────────────────────────────────────────
console.log(`
╔══════════════════════════════════════════════════════╗
║  ✅ Conversion complete!                             ║
╠══════════════════════════════════════════════════════╣
║  Files created:                                      ║
${css ? `║    📄 app/${cssFileName.padEnd(42)}║\n` : ""}║    📄 app/${routeName}/data.ts${" ".repeat(Math.max(0, 41 - routeName.length - 8))}║
║    📄 app/${routeName}/page.tsx${" ".repeat(Math.max(0, 41 - routeName.length - 9))}║
╠══════════════════════════════════════════════════════╣
║  Next steps:                                         ║
║  1. Open app/${(routeName + "/page.tsx").padEnd(39)}║
║  2. Search for "TODO" and wire up dynamic data       ║
║  3. Review app/${(routeName + "/data.ts").padEnd(38)}║
║  4. Run: npm run dev                                 ║
╚══════════════════════════════════════════════════════╝
`);
